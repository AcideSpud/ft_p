#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1);
	s2 = ft_strnew(len);
	return ((char *)ft_memcpy((void *)s2, (void *)s1, len));
}
